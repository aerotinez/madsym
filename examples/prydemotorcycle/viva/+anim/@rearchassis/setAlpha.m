function setAlpha(obj, alpha)
    setAlpha(obj.SwingArm, alpha);
    setAlpha(obj.Base, alpha);
    setAlpha(obj.Body, alpha);
    setAlpha(obj.Driver, alpha);
    setAlpha(obj.LeftArm, alpha);
    setAlpha(obj.LeftLeg, alpha);
    setAlpha(obj.RightArm, alpha);
    setAlpha(obj.RightLeg, alpha);
end