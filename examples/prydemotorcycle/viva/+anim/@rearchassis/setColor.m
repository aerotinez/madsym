function setColor(obj, color)
    setColor(obj.SwingArm, color);
    setColor(obj.Base, color);
    setColor(obj.Body, color);
    setColor(obj.Driver, color);
    setColor(obj.LeftArm, color);
    setColor(obj.LeftLeg, color);
    setColor(obj.RightArm, color);
    setColor(obj.RightLeg, color);
end